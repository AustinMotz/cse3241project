CREATE TABLE BOOK (
    Title varchar(25),
    Author varchar(40),
    Genre varchar(25),
    ReleaseDate date,
    ISBN char(13),
    InventoryID varchar(6),
    Price decimal,
    Cost decimal,
    StoreID varchar(4),
    ShipmentID varchar(4),
    TransactionID varchar(4),
    PublisherID varchar(4)
);
